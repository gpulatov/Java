
public class Assign21VipCustomer {

	private String name;
	private double creditLimit;
	private String email;
	
	public Assign21VipCustomer() {
		this.name = name;
		this.creditLimit = creditLimit;
		this.email = email;
	}
	
	public Assign21VipCustomer(String name, double creditLimit) {
		this.name = name;
		this.creditLimit = creditLimit;
		this.email = email;
	}
	
	public Assign21VipCustomer(String name, double creditLimit, String email) {
		this.name = name;
		this.creditLimit = creditLimit;
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public double getCreditLimit() {
		return creditLimit;
	}

	public String getEmail() {
		return email;
	}
	
	
	
}
